import numpy as np
import pandas as pd

np.random.seed(42)

n = 30000
Name = ['stud_'+str(i).zfill(5) for i in range(1,n+1)]

phone = np.random.randint(5555555555,9999999999,n)

mail = [i+"@gmail.com" for i in Name]

type = np.random.choice(["Incubation","Paid"],n)

Tenth_per = np.round(np.random.randint(60,99,n) + np.random.rand(n) , 2)

tenth_yop = np.random.choice([2020,2021,2022],n)

Inter_per = np.round(np.random.randint(60,99,n) + np.random.rand(n) , 2)

inter_yop = np.array([ y+2 for y in tenth_yop])

deg_stream = np.random.choice(['B.Tech','BSC','BCA','B.com','BBA'],n)

deg_per = np.round(np.random.randint(60,99,n) + np.random.rand(n) , 2)

deg_yop = np.where(deg_stream == 'B.Tech',inter_yop+4,inter_yop+3)

mas_stream = np.array([np.random.choice(["M.Tech",None]) if stream == "B.Tech" else
                      np.random.choice(["MCA",None]) if stream == "BCA" else
                       np.random.choice(["MSC",None]) if stream == "BSC" else None
                       for stream in deg_stream])

mas_per = np.where(mas_stream != None,np.random.randint(66,99),None)

mas_yop = np.where(mas_stream != None,deg_yop +2 ,None)

branch = np.random.choice(['Pyspiders','JSpiders','QSpiders','Prospiders'],n)

course = np.array([np.random.choice(['Data Analytics',"Data Science",'Python FullStack'] )if branch == 'Pyspiders' else
                  np.random.choice(["Java with Selenium","Python with Selenium"]) if branch == 'QSpiders' else
                   np.random.choice(["Banking","Soft Skills"]) if branch == 'Prospiders' else
                   np.random.choice(["Core Java","Advance Java"]) for branch in branch])

fsdi = np.random.choice(['Yes','No'],n)

placed = np.array([np.random.choice(['Placed',"Not Placed"]) if deg_yop < 2027 else
                  np.random.choice(['Placed',"Not Placed"]) if mas_yop is not None and mas_yop < 2027
                  else None for deg_yop,mas_yop in zip(deg_yop,mas_yop)])

Enroll = [np.random.choice(pd.date_range('02/05/2026' , '02/28/2026',freq = 'D'))if y == 2026 and pd.isna(m) else
          np.random.choice(pd.date_range('02/05/2027' , '02/28/2027',freq = 'D')) if y == 2027 and pd.isna(m) else
          np.random.choice(pd.date_range('02/05/2028' , '02/28/2028',freq = 'D'))if y == 2028 and pd.isna(m) else
          np.random.choice(pd.date_range('02/05/2025' , '02/28/2025',freq = 'D')) if y == 2025 and pd.isna(m) else
          np.random.choice(pd.date_range('02/05/2027' , '02/28/2027',freq = 'D')) if m == 2027 else
          np.random.choice(pd.date_range('02/05/2028' , '02/28/2028',freq = 'D')) if m == 2028 else
          np.random.choice(pd.date_range('02/05/2029' , '02/28/2029',freq = 'D'))if m == 2029 else
          np.random.choice(pd.date_range('02/05/2030' , '02/28/2030',freq = 'D'))
          for y,m in zip(deg_yop,mas_yop)]

df = pd.DataFrame({'Name': Name,
                   'Phone Number':phone,
                   'Enrollement Date':Enroll,
                   'Mail Id':mail,
                   'Type':type,
                   '10_per':Tenth_per,
                   '10_yop':tenth_yop,
                   '12_per':Inter_per,
                   '12_yop':inter_yop,
                   'deg_stream':deg_stream,
                   'deg_per':deg_per,
                   'deg_yop':deg_yop,
                   'Masters_Stream':mas_stream,
                   'Masters_per':mas_per,
                   'Masters_yop':mas_yop,
                   'Branch':branch,
                   'course':course,
                   'FSDI'  : fsdi,
                   'Status':placed
                   })

df

df.to_excel('Students Dataset.xlsx')